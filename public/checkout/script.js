function updateQuantity(id, change) {
  const item = cart.find(p => p.id === id);
  if (item) {
    item.quantity = Math.max(1, item.quantity + change);
    saveCart();
    renderCart();
  }
}

function removeItem(id) {
  cart = cart.filter(p => p.id !== id);
  saveCart();
  renderCart();
}

